"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas simples ' ' ou aspas duplas " "
"""

# Aspas simples
print('Olá, Mundo!')

# Aspas duplas
print("Olá, Mundo!")

# Escape
print("Olá, Mundo! \"Tudo bem?\"")
print('Olá, Mundo! "Tudo bem?"')

# r
print(r'Olá, Mundo! Tudo bem?')